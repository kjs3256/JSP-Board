package survey;

public class SurveyDto {
	private int num;
	private String sports;
	private String fav_team;
	private String hate_team;
	private String fav_player;
	private String hate_player;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getSports() {
		return sports;
	}
	public void setSports(String sports) {
		this.sports = sports;
	}
	public String getFav_team() {
		return fav_team;
	}
	public void setFav_team(String fav_team) {
		this.fav_team = fav_team;
	}
	public String getHate_team() {
		return hate_team;
	}
	public void setHate_team(String hate_team) {
		this.hate_team = hate_team;
	}
	public String getFav_player() {
		return fav_player;
	}
	public void setFav_player(String fav_player) {
		this.fav_player = fav_player;
	}
	public String getHate_player() {
		return hate_player;
	}
	public void setHate_player(String hate_player) {
		this.hate_player = hate_player;
	}
}
